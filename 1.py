num1=float(input("Enter first number:"))
op=input("Enter operators(+,-,*,/)")
num2=float(input("Enter second number:"))
if op=="+":
    print("Addition:",num1+num2)
elif op=="-":
    print("subtraction:",num1-num2)
elif op=="*":
    print("Multiply:",num1*num2)
elif op=="/":
    print("Division:",num1/num2)